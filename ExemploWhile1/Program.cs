using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ExemploWhile1
{
    internal class Program
    {
        static void Main(string[]args)
        {
            int i = 1;
            while (i <= 5)
            {
                Console.Write(i +" ");
                i++;
            }
        }
    }
